    /**
     * Checks if a date is a holiday in Colombia.
     * @param date Date to check
     * @returns boolean
     */
    isHoliday(date: Date): boolean {
        const year = date.getFullYear();
        const isoDate = date.toISOString().split('T')[0];

        // Check if the year exists in our database
        const yearHolidays = this.holidays[year];
        if (!yearHolidays) return false;

        return yearHolidays.includes(isoDate);
    }

    /**
     * Checks if an ISO date string matches a holiday.
     * @param isoString ISO date string (YYYY-MM-DD...)
     * @returns boolean
     */
    isHolidayISO(isoString: string): boolean {
        const date = new Date(isoString);
        // Adjustment to avoid timezone shifts when parsing YYYY-MM-DD
        const localDate = new Date(date.getTime() + date.getTimezoneOffset() * 60000);
        return this.isHoliday(localDate);
    }
